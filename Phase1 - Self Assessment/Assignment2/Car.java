public class Car {
    int id;
    String make;
    String model;
    int year;
    String color;
    double price;
    String licenseNumber;

    public Car(int id, String make, String model, int year, String color, double price, String licenseNumber) {
        this.id = id;
        this.make = make;
        this.model = model;
        this.year = year;
        this.color = color;
        this.price = price;
        this.licenseNumber = licenseNumber;
    }
}


